def _validate_url(url: str, param_name: str, environ: Mapping[str, str]) -> None:
    """Validate that a control-plane URL is well-formed and uses HTTPS.

    Rejects:
    - URLs that cannot be parsed (``ValueError``-level garbage).
    - URLs with no scheme or host.
    - Non-HTTPS URLs in production (HTTP, FTP, etc.). Sending API keys
      and signed telemetry over plaintext is a credential leak.

    In dev mode (``CHECKRD_DEV=1``), non-HTTPS URLs are allowed with a
    warning. This supports ``http://localhost:8080`` for local development.
    """
    try:
        parsed = urlparse(url)
    except Exception:
        raise ValueError(
            f"{param_name} is not a valid URL: {url!r}"
        )

    if not parsed.scheme:
        raise ValueError(
            f"{param_name} has no scheme (expected https://...): {url!r}"
        )
    if not parsed.hostname:
        raise ValueError(
            f"{param_name} has no hostname: {url!r}"
        )

    if parsed.scheme.lower() != "https":
        if _http_allowed(environ):
            logger.warning(
                "checkrd: %s uses non-HTTPS scheme %r (allowed because "
                "CHECKRD_ALLOW_INSECURE_HTTP is set). Never use HTTP in "
                "production — API keys and signed telemetry would be sent "
                "in plaintext.",
                param_name,
                parsed.scheme,
            )
            return
        raise ValueError(
            f"{param_name} must use HTTPS, got {parsed.scheme!r} in "
            f"{url!r}. Sending API keys over plaintext HTTP is a "
            f"credential leak. If you are developing locally, set "
            f"CHECKRD_ALLOW_INSECURE_HTTP=1 to allow HTTP."
        )


def _http_allowed(environ: Mapping[str, str]) -> bool:
    """Return True if non-HTTPS control-plane URLs are opted in.

    New-style flag (``CHECKRD_ALLOW_INSECURE_HTTP``) is authoritative.
    Legacy ``CHECKRD_DEV`` is honored with a ``DeprecationWarning`` — it
    used to bundle HTTP + WASM-integrity bypass into one variable; the
    split exists so a single leaked value can't disable both controls.
    """
    if environ.get(ENV_ALLOW_INSECURE_HTTP, "").strip().lower() in _TRUTHY:
        return True
    if environ.get(ENV_DEV, "").strip().lower() in _TRUTHY:
        _warn_dev_deprecated()
        return True
    return False


def _wasm_integrity_skipped(environ: Mapping[str, str]) -> bool:
    """Return True if WASM integrity verification is opted out.

    Refuses the bypass in production-looking environments unless the
    operator explicitly sets
    ``CHECKRD_I_UNDERSTAND_WASM_INTEGRITY_RISK=i-understand-the-risk``.
    The awkward acknowledgment phrase is deliberate — this flag exists
    to catch accidents (a dev env var leaking into a prod deploy), not
    to block every possible use case.

    Raises:
        CheckrdInitError: when the skip flag is set in a production-like
            environment without the acknowledgment. The message is the
            only reasonable resolution path: either unset the flag or
            add the acknowledgment env var.
    """
    requested = (
        environ.get(ENV_SKIP_WASM_INTEGRITY, "").strip().lower() in _TRUTHY
    )
    legacy = environ.get(ENV_DEV, "").strip().lower() in _TRUTHY
    if not (requested or legacy):
        return False
    if legacy:
        _warn_dev_deprecated()
    if _looks_production(environ) and not _wasm_risk_acknowledged(environ):
        # Import locally to avoid a circular import with exceptions.py at
        # module load time.
        from checkrd.exceptions import CheckrdInitError

        signal = _production_signal(environ)
        if signal is None:
            # _looks_production returned True but _production_signal didn't
            # find one — invariant violation. Raise rather than silently
            # downgrade, since this is the WASM-integrity-skip code path.
            raise RuntimeError(
                "internal invariant: _looks_production() True but "
                "_production_signal() returned None"
            )
        signal_env, signal_value = signal
        raise CheckrdInitError(
            "CHECKRD_SKIP_WASM_INTEGRITY is set in a production-looking "
            f"environment ({signal_env}={signal_value!r}). This would "
            "disable the only supply-chain defense on the WASM binary. "
            "Either:\n"
            "  1. Unset CHECKRD_SKIP_WASM_INTEGRITY (recommended — the "
            "hash file is regenerated on every build), OR\n"
            f"  2. Set {ENV_ACK_WASM_RISK}={ENV_ACK_WASM_RISK_VALUE!r} "
            "if this is an intentional emergency debugging session.\n"
            "See https://checkrd.io/errors/wasm_integrity_skip_in_prod"
        )
    return True


def _looks_production(environ: Mapping[str, str]) -> bool:
    """Return True if any common env-name convention flags production."""
    return _production_signal(environ) is not None


def _production_signal(
    environ: Mapping[str, str],
) -> Optional[tuple[str, str]]:
    """Return the (env_name, raw_value) pair that flagged production, if any."""
    for name in _PRODUCTION_SIGNAL_ENVS:
        raw = environ.get(name, "")
        if raw.strip().lower() in _PRODUCTION_ENV_VALUES:
            return name, raw.strip()
    return None


def _wasm_risk_acknowledged(environ: Mapping[str, str]) -> bool:
    """True iff the operator typed the break-glass acknowledgment phrase.

    Exact-match comparison (no trim, no case-fold). The phrase is a
    deliberate muscle-memory barrier, not a configuration knob — any
    tolerance for near-misses erodes that barrier. If an operator is
    copy-pasting with trailing whitespace or uppercasing the phrase,
    they are probably scripting around a safety check, which is
    exactly the scenario the phrase exists to catch.
    """
    return environ.get(ENV_ACK_WASM_RISK, "") == ENV_ACK_WASM_RISK_VALUE


_dev_warning_emitted = False


def _warn_dev_deprecated() -> None:
    """Emit DeprecationWarning for legacy CHECKRD_DEV env var (once per process)."""
    global _dev_warning_emitted  # noqa: PLW0603
    if _dev_warning_emitted:
        return
    _dev_warning_emitted = True
    import warnings
    warnings.warn(
        "CHECKRD_DEV is deprecated and will be removed in 1.0. "
        "It bundled two independent security controls behind one flag. "
        "Use CHECKRD_ALLOW_INSECURE_HTTP=1 (allows http:// URLs) and/or "
        "CHECKRD_SKIP_WASM_INTEGRITY=1 (test-only) instead.",
        DeprecationWarning,
        stacklevel=3,
    )


def _resolve_security_mode(
    explicit: Optional["SecurityMode"],
    environ: Mapping[str, str],
) -> "SecurityMode":
    """Collapse the security_mode argument into a resolved value.

    Precedence: explicit keyword argument > env var > default (``strict``).
    Unknown env-var values fall back to the default (not silent permissive),
    so a typo can never weaken the security posture.
    """
    if explicit is not None:
        if explicit not in ("strict", "permissive"):
            raise ValueError(
                f"security_mode must be 'strict' or 'permissive'; "
                f"got {explicit!r}"
            )
        return explicit
    raw = environ.get(ENV_SECURITY_MODE, "").strip().lower()
    if raw == "permissive":
        return "permissive"
    if raw == "strict":
        return "strict"
    if raw and raw not in ("strict", "permissive"):
        logger.warning(
            "checkrd: unrecognized %s=%r, falling back to %r (fail-closed).",
            ENV_SECURITY_MODE,
            raw,
            DEFAULT_SECURITY_MODE,
        )
    return DEFAULT_SECURITY_MODE


def _resolve_enforce(
    enforce: EnforceMode,
    environ: Mapping[str, str],
) -> Optional[bool]:
    """Collapse the ``enforce`` argument into an explicit override or ``None``.

    An explicit ``True``/``False`` from the caller always wins — env vars
    cannot silently turn enforcement off in code that asked for it. ``"auto"``
    is the only value that falls back to the env var, which in turn falls
    back to ``None`` (meaning "let wrap() decide").
    """
    if enforce is True:
        return True
    if enforce is False:
        return False
    if enforce == "auto":
        raw = environ.get(ENV_ENFORCE)
        if raw is None:
            return None
        parsed = _parse_bool(raw)
        return parsed  # None if unrecognized value -> "auto" fallback
    raise ValueError(
        f"enforce must be True, False, or 'auto'; got {enforce!r}"
    )


def _parse_bool(value: Optional[str]) -> Optional[bool]:
    """Parse a standard truthy/falsy env-var string.

    Returns ``None`` for values outside the recognized sets so callers can
    distinguish "explicitly false" from "unset or garbled". This lets the
    default path remain predictable even when an operator mis-spells
    ``CHECKRD_ENFORCE=yess``.
    """
    if value is None:
        return None
    normalized = value.strip().lower()
    if normalized in _TRUTHY:
        return True
    if normalized in _FALSY:
        return False
    return None


def derive_agent_id(env: Optional[Mapping[str, str]] = None) -> str:
    """Derive a stable agent identifier from the deployment environment.

    Tries in order:

    1. PaaS service-name environment variables (Fly, Cloud Run, Lambda,
       Heroku, Railway, Render, Koyeb, Google Cloud Functions).
    2. ``{script_name}-{short_hostname}`` — stable per host, good enough
       for bare-metal and containers with a meaningful hostname.

    Raises :class:`CheckrdInitError` if neither path produces a stable
    identifier. **This is fail-closed by design.** A random-UUID
    fallback would silently produce a different ``agent_id`` on every
    process restart in containers with randomized hostnames, which
    breaks every downstream identity guarantee:

    - Per-agent kill-switch scoping evaporates (the new identity is not
      what the operator paged on).
    - Telemetry signature verification fails server-side because the
      registered public key belongs to a different ``agent_id``.
    - Dashboards group on the volatile id, fragmenting history across
      restarts.

    Operators in environments without a stable hostname or PaaS env var
    must set :data:`ENV_AGENT_ID` (``CHECKRD_AGENT_ID``) explicitly. The
    error message names every workable option so the fix is mechanical.

    Args:
        env: Environment mapping. Defaults to ``os.environ`` for the
            normal path, overridable for tests.

    Returns:
        The derived agent identifier. Always non-empty.

    Raises:
        CheckrdInitError: when no deterministic identifier can be
            derived. ``code="agent_id_undetectable"``.
    """
    environ: Mapping[str, str] = env if env is not None else os.environ

    for key in _PAAS_ENV_KEYS:
        value = environ.get(key)
        if value and value.strip():
            return value.strip()

    hostname = _short_hostname()
    script_name = Path(sys.argv[0] or "python").stem or "python"
    if hostname:
        return f"{script_name}-{hostname}"

    # No PaaS service name, no hostname — refuse to invent one.
    # Local import: ``derive_agent_id`` runs during ``resolve()`` which
    # other modules pull in eagerly; importing CheckrdInitError at module
    # top would risk a circular import.
    from checkrd.exceptions import CheckrdInitError

    paas_keys = ", ".join(_PAAS_ENV_KEYS)
    raise CheckrdInitError(
        "checkrd: cannot derive a stable agent_id — neither the system "
        "hostname nor any known PaaS service-name env var is set. "
        "A random fallback would change on every process restart, "
        "breaking kill-switch scoping and telemetry signature verification. "
        f"Set {ENV_AGENT_ID} explicitly, or run on a platform that "
        f"populates one of: {paas_keys}.",
        code="agent_id_undetectable",
    )


def _short_hostname() -> str:
    """Return the short hostname (before the first dot), or ``""`` on failure.

    ``socket.gethostname()`` can raise ``OSError`` in bizarre container
    environments; we treat any failure as "no hostname available" rather
    than surfacing it through the init path.
    """
    try:
        name = socket.gethostname()
    except OSError:
        return ""
    if not name:
        return ""
    return name.split(".", 1)[0]
