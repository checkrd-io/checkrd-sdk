@dataclass(frozen=True)
class Settings:
    """Resolved SDK settings. Immutable.

    Produced by :func:`resolve`. Every field on this object reflects the
    final value after applying the precedence chain (explicit > env > default),
    so downstream code never has to re-check ``os.environ``.

    Attributes:
        agent_id: The resolved stable agent identifier. Always non-empty.
        api_key: The resolved control-plane API key, or ``None`` if not set.
        control_plane_url: The resolved control-plane base URL, or ``None``
            if neither an API key nor an explicit URL was provided.
        enforce_override: ``True``/``False`` if enforcement was set explicitly
            by the caller or via :data:`ENV_ENFORCE`; ``None`` means
            ``"auto"`` — :func:`checkrd.wrap` decides based on whether a
            policy source was provided.
        disabled: ``True`` if :data:`ENV_DISABLED` is set to a truthy value.
    """

    agent_id: str
    api_key: Optional[str]
    control_plane_url: Optional[str]
    enforce_override: Optional[bool]
    disabled: bool
    dashboard_url: Optional[str]
    debug: bool
    # Defaulted so downstream code that constructs Settings directly (rare —
    # `resolve()` is the public entry point) does not need updating when new
    # fields are added. New fields land at the end for this reason.
    security_mode: SecurityMode = DEFAULT_SECURITY_MODE
    #: Stripe-style date pin sent as ``Checkrd-Version`` on every
    #: control-plane request when non-empty. Lets the control plane
    #: serve old + new clients simultaneously and lets customers pin
    #: a known-good API shape across rollouts. Empty string means
    #: "follow the server default" (which in practice is the latest).
    api_version: str = ""

    @property
    def has_control_plane(self) -> bool:
        """Whether the settings are sufficient to talk to the control plane."""
        return self.api_key is not None and self.control_plane_url is not None


