#: Environment variable for the Stripe-style date-pinned API version.
#: Matches the ``{PROJECT}_API_VERSION`` convention on Stripe and
#: Anthropic's SDKs. When set, the value is stamped as
#: ``Checkrd-Version`` on every control-plane request.
ENV_API_VERSION = "CHECKRD_API_VERSION"


def resolve(
    *,
    agent_id: Optional[str] = None,
    api_key: Optional[str] = None,
    control_plane_url: Optional[str] = None,
    enforce: EnforceMode = "auto",
    debug: bool = False,
    security_mode: Optional[SecurityMode] = None,
    api_version: Optional[str] = None,
    env: Optional[Mapping[str, str]] = None,
) -> Settings:
    """Resolve SDK settings from explicit args, environment, and derived defaults.

    The returned :class:`Settings` is ready for :func:`checkrd.wrap` to
    consume. Precedence (highest to lowest) is applied per field::

        explicit keyword argument  >  environment variable  >  derived default

    Args:
        agent_id: Explicit agent identifier. Defaults to ``CHECKRD_AGENT_ID``,
            then to :func:`derive_agent_id`.
        api_key: Explicit API key. Defaults to ``CHECKRD_API_KEY``.
        control_plane_url: Explicit control-plane base URL. Defaults to
            ``CHECKRD_BASE_URL``, then to :data:`DEFAULT_BASE_URL` if
            ``api_key`` ended up set.
        enforce: ``True``/``False`` to force the mode, or ``"auto"`` (the
            default) to defer the decision to :func:`checkrd.wrap`. When
            ``"auto"`` is requested, :data:`ENV_ENFORCE` is consulted as a
            fallback.
        env: Optional environment mapping (used by tests to inject isolated
            state). Defaults to ``os.environ``.

    Returns:
        A :class:`Settings` object reflecting the resolved configuration.

    Raises:
        ValueError: If ``enforce`` is not ``True``, ``False``, or ``"auto"``.
    """
    environ: Mapping[str, str] = env if env is not None else os.environ

    disabled = _parse_bool(environ.get(ENV_DISABLED)) or False

    resolved_api_key = api_key if api_key is not None else environ.get(ENV_API_KEY) or None

    if control_plane_url is not None:
        resolved_url: Optional[str] = control_plane_url
    else:
        resolved_url = environ.get(ENV_BASE_URL)
        if not resolved_url and resolved_api_key:
            # Auto-populate when an API key is present so the zero-config
            # path works out of the box against the hosted control plane.
            resolved_url = DEFAULT_BASE_URL
        elif not resolved_url:
            resolved_url = None

    if agent_id is not None:
        resolved_agent_id = agent_id
    else:
        env_agent_id = environ.get(ENV_AGENT_ID)
        if env_agent_id and env_agent_id.strip():
            resolved_agent_id = env_agent_id.strip()
        else:
            resolved_agent_id = derive_agent_id(env=environ)

    enforce_override = _resolve_enforce(enforce, environ)

    resolved_dashboard_url = environ.get(ENV_DASHBOARD_URL) or DEFAULT_DASHBOARD_URL
    resolved_debug = debug or (_parse_bool(environ.get(ENV_DEBUG)) or False)
    resolved_security_mode = _resolve_security_mode(security_mode, environ)

    # Validate URLs: catch bad config at init time, not minutes later when a
    # telemetry batch fails to send with a confusing socket error.
    if resolved_url is not None:
        _validate_url(resolved_url, "control_plane_url", environ)
    if resolved_dashboard_url is not None:
        _validate_url(resolved_dashboard_url, "dashboard_url", environ)

    # Stripe-style ``Checkrd-Version`` pin. Explicit arg > env > "".
    # Empty string means "follow the server default" — consumers
    # omit the header entirely in that case (see
    # :func:`checkrd._platform.default_control_headers`).
    if api_version is not None:
        resolved_api_version = api_version
    else:
        resolved_api_version = environ.get(ENV_API_VERSION, "")

    return Settings(
        agent_id=resolved_agent_id,
        api_key=resolved_api_key,
        control_plane_url=resolved_url,
        enforce_override=enforce_override,
        disabled=disabled,
        dashboard_url=resolved_dashboard_url,
        debug=resolved_debug,
        security_mode=resolved_security_mode,
        api_version=resolved_api_version,
    )


