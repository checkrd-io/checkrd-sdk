"""SDK settings resolution: explicit args -> env vars -> derived defaults.

Every setting in the Checkrd Python SDK resolves through the same precedence
chain, matching the convention used by Stripe, Anthropic, OpenAI, and Sentry::

    explicit keyword argument  >  environment variable  >  derived default

Keeping the resolution logic in one place (instead of threading individual
values through the stack) makes the SDK trivial to test and means the
zero-config path ``wrap(httpx.Client())`` behaves exactly like the fully
explicit one when the corresponding environment variables are set.

Recognized environment variables:

- ``CHECKRD_API_KEY`` — authentication key for the control plane.
- ``CHECKRD_BASE_URL`` — control plane base URL (e.g. ``https://api.checkrd.io``).
- ``CHECKRD_AGENT_ID`` — stable identifier for this agent / service.
- ``CHECKRD_ENFORCE`` — ``1``/``true``/``yes``/``on`` to force enforcement,
  anything else to force observation mode. Overrides the ``"auto"`` default
  but not an explicit ``True``/``False`` passed to :func:`checkrd.wrap`.
- ``CHECKRD_DISABLED`` — ``1``/``true``/``yes``/``on`` to bypass the SDK
  entirely (no WASM init, no transport swap).
- ``CHECKRD_AGENT_KEY`` — base64 Ed25519 private key for production identity
  (consumed by :class:`checkrd.LocalIdentity.from_env`, not this module).

The fallback chain for ``agent_id`` matches what ``sentry_sdk`` and
``ddtrace`` use for service names: first an explicit env var, then a list
of well-known PaaS service-name env vars, then ``{script}-{hostname}``,
then a logged random fallback.
"""

from __future__ import annotations

import logging
import os
import socket
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Literal, Mapping, Optional, Union
from urllib.parse import urlparse

logger = logging.getLogger("checkrd")


