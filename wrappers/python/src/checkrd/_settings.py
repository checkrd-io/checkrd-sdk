#: Environment variable for the control-plane base URL. Matches the
#: ``{PROJECT}_BASE_URL`` convention used by Anthropic and OpenAI.
ENV_BASE_URL = "CHECKRD_BASE_URL"

#: Environment variable for the stable agent identifier.
ENV_AGENT_ID = "CHECKRD_AGENT_ID"

#: Environment variable for forcing enforcement on/off. Overrides
#: :data:`EnforceMode` ``"auto"`` but never an explicit ``True``/``False``.
ENV_ENFORCE = "CHECKRD_ENFORCE"

#: Environment variable to disable the SDK entirely. When set to a truthy
#: value, ``wrap()`` returns the client unchanged.
ENV_DISABLED = "CHECKRD_DISABLED"

#: Environment variable for the dashboard base URL. Used to generate
#: deep links in :class:`CheckrdPolicyDenied` error messages.
ENV_DASHBOARD_URL = "CHECKRD_DASHBOARD_URL"

#: Environment variable to enable debug logging. When truthy, the SDK
#: logs a per-request evaluation trace at DEBUG level.
ENV_DEBUG = "CHECKRD_DEBUG"

#: Environment variable to allow non-HTTPS control-plane URLs
#: (e.g. ``http://localhost:8080``). Explicit opt-in for local development.
#: **Never set this in production** — API keys and signed telemetry would be
#: sent in plaintext.
ENV_ALLOW_INSECURE_HTTP = "CHECKRD_ALLOW_INSECURE_HTTP"

#: Environment variable to skip WASM binary integrity verification.
#: **Test-only.** The hash file ``_wasm_integrity.py`` is regenerated by
#: ``scripts/copy-wasm.sh`` on every build, so production installs always
#: have it. Skipping the check disables the only supply-chain defense on
#: the WASM binary; the SDK emits a loud WARNING if this is set.
ENV_SKIP_WASM_INTEGRITY = "CHECKRD_SKIP_WASM_INTEGRITY"

#: Break-glass acknowledgment for running with
#: :data:`ENV_SKIP_WASM_INTEGRITY` in a production-looking environment.
#: The value must be the literal string ``"i-understand-the-risk"`` — a
#: deliberately awkward phrase so nobody sets it by accident. The only
#: legitimate use is an emergency dev/debugging session against a
#: production-like target; in every other case, integrity verification
#: is mandatory. Matches the break-glass override pattern used by
#: Kubernetes (``--accept-hosts``) and Sentry's ``_experiments``.
ENV_ACK_WASM_RISK = "CHECKRD_I_UNDERSTAND_WASM_INTEGRITY_RISK"
ENV_ACK_WASM_RISK_VALUE = "i-understand-the-risk"

#: Environment variables consulted, in order, to infer whether the SDK is
#: running in a production-like deployment. Any one of these set to a
#: value in :data:`_PRODUCTION_ENV_VALUES` locks down security bypasses.
#: Mirrors the signals used by Rails (``RAILS_ENV``), Django
#: (``DJANGO_SETTINGS_MODULE``), Node (``NODE_ENV``), and Sentry's
#: ``environment`` tag — no single value is authoritative, because every
#: framework spells "production" differently.
_PRODUCTION_SIGNAL_ENVS: tuple[str, ...] = (
    "CHECKRD_ENV",
    "CHECKRD_ENVIRONMENT",
    "ENVIRONMENT",
    "ENV",
    "APP_ENV",
    "NODE_ENV",
    "RAILS_ENV",
    "DJANGO_ENV",
    "FLASK_ENV",
    "PYTHON_ENV",
    "DEPLOYMENT_ENVIRONMENT",
)

#: Values (lowercased) on any of :data:`_PRODUCTION_SIGNAL_ENVS` that
#: flag the environment as production-like. "canary" and "live" are
#: included because some teams spell production differently.
_PRODUCTION_ENV_VALUES = frozenset({"production", "prod", "canary", "live"})

#: Deprecated. Previously a single flag bundling HTTP allowance and WASM
#: integrity bypass. Kept as a backwards-compat alias: when set, both
#: :data:`ENV_ALLOW_INSECURE_HTTP` and :data:`ENV_SKIP_WASM_INTEGRITY`
#: are treated as if they were set. Emits a ``DeprecationWarning``.
#:
#: Bundling these two capabilities behind one env var meant a single leaked
#: variable (CI secret, container escape) disabled two independent security
#: controls — the split exists to prevent that.
ENV_DEV = "CHECKRD_DEV"

#: Environment variable controlling fail-closed vs fail-open behavior.
#: Values:
#:
#: - ``strict`` (default): raise :class:`CheckrdInitError` on WASM engine
#:   failure. A broken security layer must not silently allow traffic.
#: - ``permissive``: log a warning and pass traffic through unchecked.
#:   Opt-in for gradual rollout — intended for teams who want to observe
#:   SDK integration issues without their app breaking.
#:
#: Explicit ``security_mode=`` argument on :func:`init`/:func:`wrap` always
#: wins over this env var.
ENV_SECURITY_MODE = "CHECKRD_SECURITY_MODE"

#: Default dashboard base URL.
DEFAULT_DASHBOARD_URL = "https://app.checkrd.io"

#: Default control-plane base URL when an API key is configured but no
#: explicit base URL is given.
DEFAULT_BASE_URL = "https://api.checkrd.io"

#: Type alias for the ``enforce`` parameter of :func:`checkrd.wrap`.
#:
#: Three values:
#:   - ``True``: always block on engine deny. Operator override.
#:   - ``False``: never block; record deny verdicts in telemetry only.
#:     Use for phased rollouts where you want to log "would have been
#:     denied" without breaking real traffic.
#:   - ``"auto"`` (default): trust the engine. Block on engine deny;
#:     since the WASM engine respects the policy's ``mode`` field
#:     internally (``mode: dry_run`` returns allowed=true even on a
#:     deny match), this never over-enforces. Mirrors how every
#:     comparable enforcement point handles it (OPA-PEP, Envoy
#:     ext_authz, Stripe Radar, AWS Config, Cloudflare WAF — policy
#:     carries the mode, the enforcement point trusts the verdict).
EnforceMode = Union[bool, Literal["auto"]]

#: Type alias for the ``security_mode`` parameter of :func:`checkrd.wrap`.
#:
#: - ``strict`` (default): fail-closed — WASM engine errors raise instead of
#:   silently degrading to pass-through. Required for any deployment that
#:   treats Checkrd as a security boundary.
#: - ``permissive``: fail-open — logs a warning and continues without
#:   policy enforcement. Opt-in for gradual rollout only.
SecurityMode = Literal["strict", "permissive"]

#: Default security mode. Fail-closed by default — the security layer
#: must not silently disable itself. Callers opt in to ``"permissive"``
#: during rollout.
DEFAULT_SECURITY_MODE: SecurityMode = "strict"

# PaaS service-name env vars checked in priority order when no explicit
# ``CHECKRD_AGENT_ID`` is set. Ordered by how precisely each one identifies
# a logical service (vs. a physical host or a one-off invocation).
_PAAS_ENV_KEYS: tuple[str, ...] = (
    "FLY_APP_NAME",             # Fly.io
    "K_SERVICE",                # Knative / Google Cloud Run
    "AWS_LAMBDA_FUNCTION_NAME", # AWS Lambda
    "HEROKU_APP_NAME",          # Heroku
    "RAILWAY_SERVICE_NAME",     # Railway
    "RENDER_SERVICE_NAME",      # Render
    "KOYEB_APP_NAME",           # Koyeb
    "FUNCTION_TARGET",          # Google Cloud Functions (2nd gen)
)

# Values accepted as a truthy boolean in env vars. Matches the convention
# used by Stripe, Django, and most Rust services. Anything not in this set
# (and not in _FALSY) means "use the default".
_TRUTHY = frozenset({"1", "true", "yes", "on"})
_FALSY = frozenset({"0", "false", "no", "off", ""})


